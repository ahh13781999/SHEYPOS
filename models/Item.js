const { Schema, model } = require("mongoose")

const ItemSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
},{
  timestamps: true
})

module.exports = model("Item", ItemSchema)
